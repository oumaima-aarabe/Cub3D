#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t			i;
	unsigned char	*adress;

	i = 0;
	adress = (unsigned char *)s;
	if (!n)
		return (NULL);
	while (i < n)
	{
		if (*adress == (unsigned char)(c % 256))
		{
			return ((unsigned char *)adress);
		}
		adress++;
		i++;
	}
	return (NULL);
}
